def _generic_section(ctx: DiscoveryContext) -> str:
    mod_names = ", ".join(m.name for m in ctx.modules)
    return f"""\
## Interfaces

Modules: {mod_names}

<!-- Define shared interfaces, schemas, and data exchange formats here.
     Examples:
     - API endpoint contracts (request/response shapes)
     - Shared data types and enums
     - File format specifications
     - Environment variable agreements
-->
"""


def _footer() -> str:
    return """\
---

## Change Protocol

1. Propose changes in a Cross-Module Request (STATUS.md)
2. Get approval from affected module owners
3. Update this file FIRST, then update implementations
4. Run integration tests to verify contract compliance
"""
