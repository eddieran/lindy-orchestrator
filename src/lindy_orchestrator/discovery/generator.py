def _write_file(path: Path, content: str, force: bool) -> list[Path]:
    """Write a file, handling conflicts. Returns list of written paths."""
    if path.exists() and not force:
        console.print(f"  [dim]{path.name} already exists, skipping[/]")
        return []

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")

    rel = path.name
    try:
        rel = str(path.relative_to(Path.cwd()))
    except ValueError:
        pass
    console.print(f"  [green]Created {rel}[/]")
    return [path]


def _update_gitignore(root: Path) -> None:
    """Ensure .gitignore has orchestrator entry."""
    gitignore = root / ".gitignore"
    entry = ".orchestrator/"

    if gitignore.exists():
        existing = gitignore.read_text(encoding="utf-8")
        if entry not in existing:
            with gitignore.open("a", encoding="utf-8") as f:
                f.write(f"\n# lindy-orchestrator\n{entry}\n")
    else:
        gitignore.write_text(
            f"# lindy-orchestrator\n{entry}\n",
            encoding="utf-8",
        )
