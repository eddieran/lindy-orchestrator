def _render_config(ctx: DiscoveryContext) -> str:
    """Render orchestrator.yaml from the discovery context."""
    lines = [
        "# orchestrator.yaml — generated by lindy-orchestrate onboard",
        "",
        "project:",
        f'  name: "{ctx.project_name}"',
        f'  branch_prefix: "{ctx.branch_prefix}"',
        "",
        "modules:",
    ]

    for mod in ctx.modules:
        lines.append(f"  - name: {mod.name}")
        lines.append(f"    path: {mod.path}/")

        # Add repo if we can extract from git remote
        if ctx.git_remote:
            repo = _extract_repo_slug(ctx.git_remote)
            if repo:
                lines.append(f"    repo: {repo}")

        # Add CI workflow if detected
        ci_file = _detect_module_ci(mod)
        if ci_file:
            lines.append(f"    ci_workflow: {ci_file}")

        lines.append("")

    lines.extend(
        [
            "planner:",
            "  mode: cli  # cli | api",
            "  # model: claude-sonnet-4-20250514  # for api mode",
            "",
            "dispatcher:",
            "  timeout_seconds: 1800",
            "  permission_mode: bypassPermissions",
            "",
        ]
    )

    # QA gates from detected commands
    qa_gates = _build_qa_gates(ctx)
    if qa_gates:
        lines.append("qa_gates:")
        lines.append("  custom:")
        for gate in qa_gates:
            lines.append(f"    - name: {gate['name']}")
            lines.append(f'      command: "{gate["command"]}"')
            lines.append(f'      cwd: "{gate["cwd"]}"')
        lines.append("")

    lines.extend(
        [
            "safety:",
            "  dry_run: false",
            "  max_retries_per_task: 2",
            f"  max_parallel: {min(len(ctx.modules), 3)}",
        ]
    )

    return "\n".join(lines) + "\n"


def _extract_repo_slug(remote_url: str) -> str:
    """Extract org/repo from a git remote URL."""
    # SSH: git@github.com:org/repo.git
    if ":" in remote_url and "@" in remote_url:
        path = remote_url.split(":")[-1]
        return path.removesuffix(".git")
    # HTTPS: https://github.com/org/repo.git
    if "github.com" in remote_url or "gitlab.com" in remote_url:
        parts = remote_url.rstrip("/").split("/")
        if len(parts) >= 2:
            repo = parts[-1].removesuffix(".git")
            org = parts[-2]
            return f"{org}/{repo}"
    return ""


def _detect_module_ci(mod) -> str:
    """Detect CI workflow file for a module."""
    # The CI is usually at root level, not per-module
    # Return a reasonable default
    return "ci.yml"


def _build_qa_gates(ctx: DiscoveryContext) -> list[dict[str, str]]:
    """Build QA gate configurations from discovery context."""
    gates = []
    for mod_name, commands in ctx.qa_requirements.items():
        for cmd in commands:
            # Clean up comment annotations from command detection
            clean_cmd = cmd.split("  #")[0].strip()
            gate_name = f"{mod_name}-{_slugify(clean_cmd)}"
            gates.append(
                {
                    "name": gate_name,
                    "command": clean_cmd,
                    "cwd": "{module_path}",
                }
            )
    return gates


def _slugify(text: str) -> str:
    """Create a simple slug from a command string."""
    # Take first word after common prefixes
    text = text.replace("npx ", "").replace("npm run ", "").replace("npm ", "")
    parts = text.split()
    if parts:
        slug = parts[0].replace(".", "").replace("/", "")
        return slug[:20]
    return "check"


